clc;
clear;

t=0:0.01:10;
s1=sin(2*pi*t);
s2=sin(10*pi*t);
s3=sin(20*pi*t);
s=s1+s2+s3;
figure(1);
subplot(3,1,1);
plot(t,s1);
title('输入信号 s1');
xlabel('时间 t');
ylabel('幅度');
subplot(3,1,2);
plot(t,s2);
title('输入信号 s2');
xlabel('时间 t');
ylabel('幅度');
subplot(3,1,3);
plot(t,s3);
title('输入信号 s3');
xlabel('时间 t');
ylabel('幅度');
figure(2);
plot(t,s);
title('混合信号');
xlabel('时间 t');
ylabel('幅度');